#include<stdio.h>

int main()
{
    char ch = 'A';
    int i = 11;
    float f = 90.99f;
    double d = 78.67890;

    printf("Displaying R Values of all Data Types\n\n");

    printf("Character : %c\n",ch);
    printf("Integer : %d\n",i);
    printf("Float : %f\n",f);
    printf("Double : %lf\n",d);

    return 0;
}